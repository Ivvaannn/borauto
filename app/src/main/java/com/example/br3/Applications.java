package com.example.br3;

public class Applications {
    public String id, car, date, descriptions, equipment, price, idUsers, img1, img2, img3, status, komment;

    public Applications() {
    }

    public Applications(String id, String car, String date, String descriptions, String equipment, String price, String idUsers, String img1, String img2, String img3, String status, String komment) {
        this.id = id;
        this.car = car;
        this.date = date;
        this.descriptions = descriptions;
        this.equipment = equipment;
        this.price = price;
        this.idUsers = idUsers;
        this.img1 = img1;
        this.img2 = img2;
        this.img3 = img3;
        this.status = status;
        this.komment = komment;
    }
}
